"""
表示名の標準化定義。

内部処理はLegacy列名を維持し、UI/README/Excel表示のみを変換する。
"""

DISPLAY_LABELS = {
    "件名": "問い合わせタイトル",
    "カテゴリ": "問い合わせ分類",
    "概要": "問い合わせ内容",
    "対応結果": "回答内容",
    "リンク名": "参考リンク・資料名",
    "生_概要": "入力_問い合わせ内容",
    "生_対応結果": "入力_回答内容",
    "生_対応結果_文字数": "入力_回答内容_文字数",
    "変換後_質問": "生成_FAQ質問",
    "変換後_回答": "生成_FAQ回答",
    "質問": "生成_FAQ質問",
    "回答": "生成_FAQ回答",
}


def display_label(label: str) -> str:
    """表示名を標準化する。未定義はそのまま返す。"""
    return DISPLAY_LABELS.get(label, label)

